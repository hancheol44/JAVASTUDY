package day01;

public class Test03 {
	public static void main(String[] args) {
		/*
		 	대입 연산자
		 		데이터를 변수에 기억시킬때 사용하는 연산자
		 		
		 		종류]
		 			=, +=, -=, *=, /=, %=
		 */
		// =
		int no1 = 10;
		// +=
		int no2 = 10;

		no2 += 10;    //no2 = no2 + 10;
		System.out.println(no2);
		// -=
		int no3 = 10;
		no3 -= 10;   // no3 = no3 - 10;
		System.out.println(no3);
		// *=
		int no4 = 2;
		no4 *= 3;
		System.out.println(no4);
		
		// /=
		int no5 = 10;
		no5 /= 3;
		System.out.println(no5);
		
		// %=
		int no6 = 10;
		no6 %= 3;
		System.out.println(no6);
	}
}
